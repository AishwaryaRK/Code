def fourSumCount(A, B, C, D):
    sum1 = []
    sum2 = []
    for a in A:
        for b in B:
            sum1.append(a + b)
    for c in C:
        for d in D:
            sum2.append(c + d)
    cnt = 0
    for s1 in sum1:
        for s2 in sum2:
            if s1 + s2 == 0:
                cnt += 1
    return cnt


